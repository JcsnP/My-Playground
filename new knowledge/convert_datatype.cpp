#include <iostream>
using namespace std;

int main(){
	int num = 10;
	float gpa = 3.90;
	char A = 'A';
	
	cout << "float(num): " << float(num) << endl;
	cout << "num: " << num << endl;
	cout << "int(gpa): " << int(gpa) << endl;
	cout << "gpa: " << gpa << endl;
	cout << "int(A): " << int(A) << endl;
	cout << "A: " << A << endl;
}

/* OUTPUT
* float(num): 10
* num: 10
* int(gpa): 3
* gpa: 3.9
* int(A): 65
* A: A
*/
